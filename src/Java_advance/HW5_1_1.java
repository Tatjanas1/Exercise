package Java_advance;

public class HW5_1_1 {
    public static int sumOf2 (int a, int b){
        return a + b;
    }
    public static int minusOf2 (int a, int b){
        return a - b;
    }
    public static int multiplOf2 (int a, int b){
        return a * b;
    }
    public static double devisionOf2 (int a, int b){
        return a / Double.valueOf(b);
    }
}
