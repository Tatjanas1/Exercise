package Java_advance.HW_7;

public class Month72 {
    private String month;
    private int days;
    private int workDays;

    public Month72(String month, int days, int workDays) {
        this.month = month;
        this.days = days;
        this.workDays = workDays;
    }

    public String getMonth() {
        return month;
    }

    public int getDays() {
        return days;
    }

    public int getWorkDays() {
        return workDays;
    }
}
